def rewrite(value: str, speaker: str, question_number: int, turn_index: int) -> str:
    if turn_index == 0:
        value = re.sub(r"^「[^」]+」(?:——|。)?", "", value).lstrip()
    for old, new in REPLACEMENTS:
        value = value.replace(old, new)
    value = value.replace("見比べべ", "見比べ")
    value = value.replace("確かめめ", "確かめ")
    value = value.replace("切り分けてて", "切り分けて")
    lead_options = LEADS.get(speaker, LEADS["バイオリニスト"])
    lead = lead_options[(question_number + turn_index) % len(lead_options)]
    if not value.startswith(lead):
        value = f"{lead}\n\n{value}"
    bridge_options = BRIDGES.get(speaker, BRIDGES["バイオリニスト"])
    bridge = bridge_options[(question_number * 3 + turn_index) % len(bridge_options)]
    if bridge not in value:
        value = f"{value}\n\n{bridge}"
    return value


def main() -> None:
    changed = []
    for question_number in range(86, 114):
        path = ROOT / "chapters" / f"q{question_number:03d}.json"
        data = json.loads(path.read_text())
        for turn_index, turn in enumerate(data["discussion"]):
            turn["plain"] = rewrite(
                turn["plain"], turn["speaker"], question_number, turn_index
            )
        path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n")
        changed.append(path.name)
    print(json.dumps(changed, ensure_ascii=False))


if __name__ == "__main__":
    main()
